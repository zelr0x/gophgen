// Package imgen defines constants used by image generator
// as well as methods of creating images with different parameters.
// It also contains color palettes.
package imgen

import (
	"image/color"
	"math/rand"
)

// palette maps color names to their respective color.RGBA values.
type palette map[string]color.RGBA

// palette.getColor encapsulates underlying type of palette.
// Returns color and bool - true if color was found, false otherwise.
func (p palette) getColor(name string) (color.RGBA, bool) {
	color, exists := p[name]
	return color, exists
}

// palette.random returns a random color.RGBA from palette.
func (p palette) random() color.RGBA {
	i := rand.Intn(len(p))
	for k := range p {
		if i == 0 {
			return p[k]
		}
		i--
	}
	// This should never happen but it's not worth the struggle
	// of returning nil and / or error, let alone panicking.
	return p["red"]
}

